<h1>Повод к началу войны</h1>
<p class=first4> "Толчком" к началу Первой мировой войны послужили события в Сараево (Босния и Герцеговина). 28 июня 1914 года член организации «Черная рука» движения «Молодая Босния» Гаврило Принцип убил эрцгерцога Франса Фердинанда. Фердинанд был наследником австро-венгерского престола, поэтому резонанс у убийства был громадный. Это был повод Австро-Венгрии напасть на Сербию.</p>
<img class ="dead" id="img" src="img/img2.jpg" alt="Убийство эрцгерцога Франса Фердинанда" />
<p class=first2> Здесь очень важно поведение Англии, поскольку Австро-Венгрия самостоятельно не могла начать войну, ведь это практически гарантировало войну во всей Европе. Англичане на уровне посольства убеждали Николая 2, что Россия в случае агрессии не должна оставлять Сербию без помощи. Но тут же вся английская пресса писала о том, что сербы варвары и Австро-Венгрия не должна оставлять безнаказанным убийство эрцгерцога. То есть, Англия сделала все, чтобы Австро-Венгрия, Германия и Россия не уклонялись от войны.</p>

