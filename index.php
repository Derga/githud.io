<h1>Страны – участницы Первой мировой войны</h1>
<img  id="img1" src="img/img1.jpg" alt="" width="600" height="460"/>


<div class=first3> 
Война происходила между двумя группами стран:
<ul>
   <li> <span>АНТАНТА: </span>Российская империя, Франция, Великобритания. Союзниками выступали США, Италия, Румыния, Канада, Австралия, новая Зеландия.</li>
   <li><span> ТРОЙСТВЕННЫЙ СОЮЗ: </span>Германия, Австро-Венгрия, Османская империя. Позже к ним присоединилось Болгарское царство, и коалиция стала именоваться «Четверной союз».</li>
</ul>

<p>В войне принимали участие следующие крупные страны: Австро-Венгрия (27 июля 1914 - 3 ноября 1918), Германия (1 августа 1914 - 11 ноября 1918), Турция (29 октября 1914 - 30 октября 1918), Болгария (14 октября 1915 - 29 сентября 1918). Страны Антанты и союзники: Россия (1 августа 1914 - 3 марта 1918), Франция (3 августа 1914), Бельгия (3 августа 1914), Великобритания (4 августа 1914), Италия (23 мая 1915), Румыния (27 августа 1916).
</p>
<p>Изначально членом "Тройственного союза" также была Италия. Но после начала Первой мировой войны, итальянцы объявили о нейтралитете.
</p>
