<h1>   
Причины войны и интересы сторон
</h1>
 <p class=first4> Главной причиной Первой мировой войны явилась борьба за передел мира. Колониальная система сошла на нет, и мировым державам просто негде было брать ресурсы. Кроме того на политической арене появляется молодое сильное государство — Германия, с которым приходилось считаться. На данном фоне возникают противоречия:</p>

<div strstr>
 <span class = "str">Англия:</span>
<ul class = "prich">
<li>вмешательство Германии в экономические дела Восточной и Юго-Западной Африки, сфер влияния Великобритании;</li>
<li>поддержки буров Германией в войне с Англией;</li>
<li>сохранение морского и колониального могущества;</li>
<li>захват у Османской Империи Месопотамии и Палестины, которые были богаты нефтью. В данном вопросе главным конкурентом Великобритании была снова Германия.</li>
</ul>
</div>

<div strstr>
 <span class = "str">Франция:</span>
<ul class = "prich">
<li>противоречия в экономической политике;</li>
<li>желание вернуть в свой состав Эльзас и Лотарингию;</li>
<li>притязания на Саарский угольный бассейн.</li>
</ul>
</div>

<div strstr>
 <span class = "str">Российская Империя:</span>
<ul class = "prich">
<li>контроль за проливами Черного моря (Басфор и Дарданеллы). Россия боялась, что Турция займет лидирующие позиции в Черноморских проливах, особенно когда командующим турецкой армией был назначен немецкий генерал фон Сандерс;</li>
<li>защита славянских народов на Балканах. Австро-Венгрия с Германией агрессивно действовали на Балканском полуострове, не слишком считаясь с мнением Российской Империи;</li>
<li>противоречия с Берлином из-за строительства железной дороги Берлин — Багдад;</li>
<li>присоединить к землям Империи Галицию.</li>
</ul>
</div>


<div strstr>
 <span class = "str">Сербия:</span>
<ul class = "prich">
<li>получить полную независимость.</li>
</ul>
</div>

<div strstr>
 <span class = "str">Болгария:</span>
<ul class = "prich">
<li>закрепить свои позиции на Балканах.</li>
</ul>
</div>

<div strstr>
 <span class = "str">Австро-Венгрия:</span>
