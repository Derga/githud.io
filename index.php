<h1>Первая мировая война (1914 - 1918)</h1>

<div class = "first">
   <p class = "first1">Первая мировая война — один из двух мощнейших и самых страшных вооруженных конфликтов в человеческой истории. В нее были вовлечены несколько стран, каждая из которых серьезно пострадала. Первая мировая война продлилась с 28 июля 1914 по 11 ноября 1918. Факты говорят о том, что за один только первый год войны в военных действиях приняли участие 70 миллионов человек, из них 60 миллионов в Европе, погибло от 9 до 10 миллионов человек. Среди гражданского населения также миллионы жертв. По разным данным от 7 до 12 миллионов мирных граждан погибли в Первой мировой войне, а 55 миллионов человек пострадали.
 
<blockquote class="blockquote-8">
    <p> Ни один человек на свете не поверил бы, если бы ему сказали в 1914 году, что тогдашние тринадцатилетние дети окажутся участниками войны, что через четыре года она будет в полном разгаре…
</p>
    <cite>— Владимир Дмитриевич Набоков, один из лидеров Конституционно-демократической партии (кадетов), отец писателя Владимира Набокова, которому на момент начала войны было 15 лет</cite>
</blockquote>
</p>  
  <p  style = "margin-bottom: 20px;" class=first2> Участниками Первой Мировой войны 1914-1918 годов стали 38 из 59 существовавших на тот момент государств. Эта война навсегда изменила политическую карту мира и ход истории человечества.

   </p>
</div>
