<li>Урегулирование революционных движений в некоторых странах. Особенно данный вопрос был актуален для Российской Империи, которая переживала сложное внутриполитическое время.</li>
<li>Кроме всего прочего: научно-технический прогресс не стоял на месте и изобреталось новое оружие, которое было использовано для демонстрации мощи крупных и новых государств. Во время Первой мировой войны было впервые использовано химическое оружие, ядовитые газы, танки, огнеметы. Кроме того появились первые противогазы.</li>
